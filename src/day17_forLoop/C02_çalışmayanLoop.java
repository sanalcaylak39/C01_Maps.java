package day17_forLoop;

public class C02_çalışmayanLoop {

    public static void main(String[] args) {
        for (int i = 1; i >20 ; i++) {
            System.out.println(i);
        }
        System.out.println("loop çalışmaz koşuldan dolayı");
    }
    //cte olmaz, kod koçalışmıyor değil loop sona erer çalışamadan
}
